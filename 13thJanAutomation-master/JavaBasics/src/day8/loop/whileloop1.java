package day8.loop;

public class whileloop1 {

	public static void main(String[] args) {
		int i=1;
		System.out.println("printing no from 1 to 5");
		while (i<=5) {
			System.out.println("hello "+i);
			i++;
		}
		i=5;
		System.out.println("printing no from 5 to 1");
		while (i>=01) {
			System.out.println("hello "+i);
			i--;
	}
}
}